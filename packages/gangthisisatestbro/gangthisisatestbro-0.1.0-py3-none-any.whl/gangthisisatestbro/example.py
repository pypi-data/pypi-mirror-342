"""Example module providing simple functionality."""


def get_greeting(name):
    """Return a greeting message.
    
    Args:
        name (str): Name to include in the greeting
        
    Returns:
        str: The greeting message
    """
    return f"Hello, {name}!"


def add_numbers(a, b):
    """Add two numbers and return the result.
    
    Args:
        a (int): First number
        b (int): Second number
        
    Returns:
        int: Sum of a and b
    """
    return a + b 